#include<stdio.h>
void main()
{
	int a=10,b=5;
	b+=a;//b=b+a
	printf("\n value of a :%d",a);
	printf("\n value of a :%d",b);
	
	a-=b;//a=a-b
	printf("\n value of a :%d",a);
	printf("\n value of a :%d",b);
	
	b/=a;//b=b/a
	printf("\n value of a :%d",a);
	printf("\n value of a :%d",b);
	
	a*=b;//a=a*b
	printf("\n value of a :%d",a);
	printf("\n value of a :%d",b);
	
	a%=b;//a=a%b
	printf("\n value of a :%d",a);
	printf("\n value of a :%d",b);
	
	getch();
}
	
